# Booleans / Logical
x_true = True
x_false = False
print(type(x_true))
print(type(x_false))
print('# ---------------------')
print(1 > 2)
print(1 < 2)
print(1 == 2)
print(1 == 1)
print('# ---------------------')
x_number1 = 5
y_number2 = 10
print(f"Less then:\t\t {x_number1 > y_number2}")
print(f"Greater:\t\t {x_number1 < y_number2}")
print(f"Not equal:\t\t {x_number1 != y_number2}")
print(f"Not greater:\t\t {not(x_number1 > y_number2)}")
print(f"Or: {x_number1 or y_number2}")
print(f"And: {x_number1 and y_number2}")
print('# ---------------------')